grep -q '^## Formal Core$' "$readme"

grep -q 'root-level execution kernel' "$readme"
grep -q '`shift.Runtime` ownership, `shift.run(...)`, and the public result/error' "$readme"
grep -q '`shift.RuntimeError`,' "$readme"
grep -q '`shift.ErrorWitnessV1`, `shift.Decl`, `shift.Op`, `shift.Decision(...)`,' "$readme"
grep -q '`shift.Program(...)`, and `shift.run(...)`' "$readme"
grep -q 'current authoring frontends lower into that kernel; they are implementation' "$readme"
grep -q 'witness ids, bridge case ids, source corpus ids, and legacy fixture names are' "$readme"
grep -q 'retired root spellings are checked by tombstone proofs instead of' "$readme"
grep -q 'compatibility narratives' "$readme"
grep -q 'Retired root spellings are gone from the shipped root' "$readme"
grep -q 'no public continuation handle is exported' "$readme"

! grep -q '`Program.Manifest`' "$readme"
! grep -q 'shift.with(' "$readme"
! grep -q 'shift.effect.' "$readme"
! grep -q 'shift.algebraic.' "$readme"
! grep -q 'shift.ordinary' "$readme"
! grep -q 'ordinary-Zig experimental track' "$readme"
! grep -q 'compatibility layer' "$readme"
! grep -q 'legacy-compatible' "$readme"
! grep -q 'shift.runWith(' "$readme"

grep -q 'zig build source-lowering-coverage-check' "$readme"
